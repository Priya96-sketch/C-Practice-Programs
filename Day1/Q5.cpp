// Print the size of int, char, float and double. //
#include<iostream>
using namespace std;
int main()
{
    cout << "int size: " <<sizeof(int)<<endl;
    cout << "char size: " <<sizeof(char)<<endl;
    cout << "float size: " <<sizeof(float)<<endl;
    cout << "double size: " <<sizeof(double)<<endl;
    return 0;
    
}
